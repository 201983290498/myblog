package com.coder.commom.annotation.Enum;

/**
 * The enum Resource type.
 */
public enum ResourceType {

    /**
     * 获取网页
     */
    HTML,

    /**
     * 获取图片资料
     */
    IMAGE,

    /**
     * 获取blog
     */
    BLOG,

    /**
     * 获取一些记录信息,例如用户列表,图片列表,资源列表
     */
    RECORD,

    /**
     * 对资源的一种操作
     */
    CHECK,

    /**
     * 对资源的一种操作，修改
     */
    MODIFY
}
